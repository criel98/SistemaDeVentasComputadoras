    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        String nombre = Nombre.getText().trim();
        String apellido = Apellido.getText().trim();
        String correo = email.getText().trim();
        String direc = direccion.getText().trim();
        String documento = LecturaDF.getText().trim();
        String tipoDoc = DNI.isSelected() ? "DNI" : (RUC.isSelected() ? "RUC" : "");

        if (nombre.isEmpty() || apellido.isEmpty() || correo.isEmpty()
                || direc.isEmpty() || documento.isEmpty() || tipoDoc.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Todos los campos son obligatorios.");
            return;
        }

        if (DNI.isSelected() && !documento.matches("\\d{8}")) {
            JOptionPane.showMessageDialog(this, "El DNI debe tener 8 d√≠gitos.");
            return;
        }

        if (RUC.isSelected() && !documento.matches("\\d{11}")) {
            JOptionPane.showMessageDialog(this, "El RUC debe tener 11 d√≠gitos.");
            return;
        }

        Cliente c = new Cliente(documento, tipoDoc, nombre, apellido, direc, correo);
        GestionClientes.agregarCliente(c);

        JOptionPane.showMessageDialog(this, "Cliente registrado correctamente.");
        limpiarCampos();
    }

